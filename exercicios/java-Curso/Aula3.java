package com.mycompany.cursojava;
public class Aula3 {
    public static void main(String[] args) {
        //variavel
        int pos = 1;
        
        switch(pos){
            case 1 :
                System.out.println("Primeiro lugar");
            break; 
            
            case 2 :
                System.out.println("Segundo lugar");
            break;
            
            case 3 :
                System.out.println("Terçeiro lugar");
            break;
            
            default:
                System.out.println("Não ficou no podio");
            break;
                
        }
        
    }//psvm
}//classe principal
